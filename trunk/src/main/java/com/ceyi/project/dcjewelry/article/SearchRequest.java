package com.ceyi.project.dcjewelry.article;

import pw.wecode.project.framework.jdbc.Page;

import java.util.Date;

/**
 * Created by lingh on 2017/3/30.
 */
public class SearchRequest extends Page {
    private String keyword;
    private String tag;
    private int cid;
    private boolean old;
    private int id;
    private Date updateTime;
    private String functionType;

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public boolean isOld() {
        return old;
    }

    public void setOld(boolean old) {
        this.old = old;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public String getFunctionType() { return functionType; }

    public void setFunctionType(String functionType) { this.functionType = functionType; }

    @Override
    public String toString() {
        return "SearchRequest{" +
                "pageNumber='" + this.getPageNumber() + '\'' +
                ", pageSize='" + this.getPageSize() + '\'' +
                ", keyword='" + keyword + '\'' +
                ", tag='" + tag + '\'' +
                ", cid=" + cid +
                ", old=" + old +
                ", id=" + id +
                ", updateTime=" + updateTime +
                ", functionType=" + functionType +
                '}';
    }
}
