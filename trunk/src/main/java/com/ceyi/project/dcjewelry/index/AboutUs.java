package com.ceyi.project.dcjewelry.index;

import pw.wecode.project.framework.jdbc.Table;

/**
 * Created by dz on 2017/8/10.
 */
@Table("aboutus")
public class AboutUs {
    private int id;
    private String name;
    private String src;
    private String content;
    private String contact_way;
    private String email;
    private String fax;
    private String web;
    private String address;

    public int getId() { return id; }

    public void setId(int id) { this.id = id; }

    public String getName() { return name; }

    public void setName(String name) { this.name = name; }

    public String getSrc() { return src; }

    public void setSrc(String src) { this.src = src; }

    public String getContent() { return content; }

    public void setContent(String content) { this.content = content; }

    public String getContact_way() { return contact_way; }

    public void setContact_way(String contact_way) { this.contact_way = contact_way; }

    public String getEmail() { return email; }

    public void setEmail(String email) { this.email = email; }

    public String getFax() { return fax; }

    public void setFax(String fax) { this.fax = fax; }

    public String getWeb() { return web; }

    public void setWeb(String web) { this.web = web; }

    public String getAddress() { return address; }

    public void setAddress(String address) { this.address = address; }

    @Override
    public String toString(){
        return "AboutUs{" +
                "id=" + id +
                ", name=" + name +
                ", src=" + src +
                ", content='" + content + '\'' +
                ", contact_way =" + contact_way +
                ", email=" + email +
                ", fax=" + fax +
                ", web=" + web +
                ", address=" + address +
                '}';
    }
}
